				<justify>SMK PENDA 2 Karanganyar

Bila kita cermati, SMK Penda 2 Karanganyar merupakan Sekolah Kejuruan yang teruji, dengan kualitas lulusan yang memiliki kemampuan khusus yang sangat berbeda dengan sekolah2 pada umumnya. Sebagai SMK pengalaman di Karanganyar system pendidikan kami bukan hanya sekedar memberi . Yakni system pendidikan menuju sekolah kerja melalui bimbingan para guru yang bermutu, maka para lulusan harus mampu bersaing dengan sekolah lain dalam hal mencari pekerjaan.

PROGRAM SMK PENDA 2 KARANGANYAR

SMK PENDA 2 Karanganyar mempunyai Program Jurusan :

1) Teknik Mesin Otomotif

Setelah menyelesaikan program pendidikan Tekinik Otomotif diharapkan siswa mampu :

a)      Mengenal , merawat dan mampu menggunakan peralatan perbengkelan sepeda motor/mobil.

b)      Memperbaiki memelihara kendaraann bermotor

c)      Menjadi ahli mesin kendaraan bermotor

2) Teknik Mekanik Industri

Setelah menyelesaikan program Teknik Mesin Industri diharapkan siswa mampu :

a)      Menjadi operator mesin di perusahaan

b)      Memperbaikan dan mengoperasikan mesin-mesin produksi

c)      Menjadi ahli mesin industri

3) Tata Busana

Setelah Menyelesaiakn Program Tata Busana Diharapkan Siswa Mampu:

a) Menjadi Perancang Mode

b) Menjadi Pengusahan Konveksi Profesional

c) Menjadi Perancang dan Produsen Seragam


